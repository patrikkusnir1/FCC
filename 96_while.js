// Iterate with JavaScript While Loops

const ourArray = [];
let i = 0;

while (i < 5) {
    ourArray.push(i); // consecute it 4 times
    i++;
}


// Add the numbers 5 through 0 (inclusive) in descending order to myArray using a while loop.
const myArray = [];
let a = 5;

while (a >= 0) {
    myArray.push(a);
    a--;
}

console.log(myArray);
