// Iterate with JavaScript For Loops

const ourArray = [];
for (let i = 0; i < 5; i++) {
    ourArray.push(i)
}
console.log(ourArray) // [0, 1, 2, 3, 4]

// Use a for loop to push the values 1 through 5 onto myArray.

const myArray = [];
for (let i = 1; i <= 5; i++) {
    myArray.push(i)
}

console.log(myArray);